public class for_loop {
    public static void main(String[] args) {
        System.out.println("print even number using for loop");

        for (int i = 0; i <= 5; i++) {
            System.out.println(2 * i + 1);
        }
        ;
        System.out.println("reverse order odd number");

        for (int i = 100; i > 0; i--) {
            System.out.println(2*i);
        }
    }
}
