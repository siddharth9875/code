public class method {
    static int logic(int x, int y) {
        int z;
        if (x > y) {
            return z = (x + y) * 5;
        } else {
            return z = x + y;
        }
    }

    public static void main(String[] args) {
        int check = logic(2, 1);
        System.out.println(check);
    }
}
